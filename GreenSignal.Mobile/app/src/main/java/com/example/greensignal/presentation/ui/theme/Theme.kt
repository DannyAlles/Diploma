private val LightColorScheme = lightColorScheme(
    primary = PrimaryGreen,
    secondary = SecondaryGreen,
    tertiary = TertiaryGreen,
    background = BackgroundGreen,
    error = ErrorRed,
    outline = OnPrimaryGreen

    /* Other default colors to override
